import { formatDecimals } from "./formatDecimals"

describe("formatDecimals", () => {
  it("works", () => {
    expect(formatDecimals("0.0000001")).toEqual("< 0.0001")
    expect(formatDecimals("0.01")).toEqual("0.01")
    expect(formatDecimals("0.0100000001")).toEqual("0.01")
    expect(formatDecimals("1000.0100000001")).toEqual("1,000")
    expect(formatDecimals("5.403")).toEqual("5.403")
    expect(formatDecimals("5.403", 10)).toEqual("5.403")
    expect(formatDecimals("5.403", 12)).toEqual("5.403")
    expect(formatDecimals("1")).toEqual("1")
    expect(formatDecimals("12")).toEqual("12")
    expect(formatDecimals("123")).toEqual("123")
    expect(formatDecimals("1234")).toEqual("1,234")
    expect(formatDecimals("12345")).toEqual("12.35K")
    expect(formatDecimals("123456")).toEqual("123.5K")
    expect(formatDecimals("1234567")).toEqual("1.235M")
    expect(formatDecimals("12345678")).toEqual("12.35M")
    expect(formatDecimals("123456789")).toEqual("123.5M")
    expect(formatDecimals("1", 0)).toEqual("1")
    expect(formatDecimals("12", 0)).toEqual("12")
    expect(formatDecimals("123", 0)).toEqual("123")
    expect(formatDecimals("1234", 0)).toEqual("1,234")
    expect(formatDecimals("12345", 0)).toEqual("12.35K")
    expect(formatDecimals("123456", 0)).toEqual("123.5K")
    expect(formatDecimals("1234567", 0)).toEqual("1.235M")
    expect(formatDecimals("12345678", 0)).toEqual("12.35M")
    expect(formatDecimals("123456789", 0)).toEqual("123.5M")
  })
})
